class Operators
  {
    public static void main(String args[])
    {
      int a = 22;
      int b = 5;
      System.out.println("Arithmetic Operator");
      System.out.println(a+b);
      System.out.println(a-b);
      System.out.println(a*b);
      System.out.println(a/b);
      System.out.println(a % b);      

      System.out.println("Relational Operator");
      System.out.println(a += b);
      System.out.println(a -= b);
      System.out.println(a *= b);
      System.out.println(a /= b);
      System.out.println(a %= b);      

    }
  }
